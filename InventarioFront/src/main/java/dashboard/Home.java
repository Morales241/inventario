    private void agregarEventosHoverABoton(JButton boton, Icon iconoEntered, Icon iconoExited, Color colorBackground, Color colorFont) {

        boton.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseEntered(MouseEvent e) {
                boton.setIcon(iconoEntered);
                boton.setBackground(colorBackground);
                boton.setForeground(colorFont);
            }

            @Override
            public void mouseExited(MouseEvent e) {
                boton.setIcon(iconoExited);
                boton.setBackground(Color.WHITE);
                boton.setForeground(Color.BLACK);
            }
        });

    }

    /**
     * Crea un icono SVG y fuerza que sea de un color específico.
     *
     * @param ruta Ruta dentro de resources (ej: "iconos/usuario.svg")
     * @param ancho Ancho deseado
     * @param alto Alto deseado2
     * @param colorDeseado El color que quieres (ej: Color.RED, Color.BLUE)
     * @return El icono coloreado listo para usar.
     */
    public FlatSVGIcon crearIconoColoreado(String ruta, int ancho, int alto, Color colorDeseado) {

        FlatSVGIcon icono = new FlatSVGIcon(ruta, ancho, alto);

        icono.setColorFilter(new FlatSVGIcon.ColorFilter(c -> {
            return colorDeseado;
        }));

        return icono;
    }

