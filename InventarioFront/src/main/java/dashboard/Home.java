    private void btnSubMenuOrganizacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubMenuOrganizacionActionPerformed

        int indexBoton = 4;

        if (!menuDesplegado) {
            this.pnlMenu.add(btnSub1, indexBoton + 1);
            this.pnlMenu.add(btnSub2, indexBoton + 2);
            this.pnlMenu.add(btnSub3, indexBoton + 3);

            menuDesplegado = true;

        } else {
            this.pnlMenu.remove(btnSub1);
            this.pnlMenu.remove(btnSub2);
            this.pnlMenu.remove(btnSub3);

            menuDesplegado = false;
        }

        this.pnlMenu.revalidate();
        this.pnlMenu.repaint();
    }//GEN-LAST:event_btnSubMenuOrganizacionActionPerformed

